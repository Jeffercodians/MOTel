    private void onVanillaGameEvent(VanillaGameEvent event) {
        LOGGER.info("Got vanilla game event " + event.getVanillaEvent() + event.getContext());
        LOGGER.info("Entity class " + event.getContext().sourceEntity().getClass().getName());



        if (event.getVanillaEvent() == GameEvent.BLOCK_CHANGE &&
            event.getContext().sourceEntity() instanceof Bee)
        {
            LOGGER.info("IT IS THE THINGS" + event.getCause() + ", " + event.getContext().sourceEntity());
            BlockEntity derp = event.getLevel().getBlockEntity(event.getCause().blockPosition());
            Bee bee = (Bee) event.getContext().sourceEntity();
            BeehiveBlockEntity beehive = (BeehiveBlockEntity) event.getLevel().getBlockEntity(bee.getHivePos());
            LOGGER.info("Here be bees " + beehive.serializeNBT().get("Bees"));

            if (derp instanceof BeehiveBlockEntity) {
                LOGGER.info("HEY IT'S A BEEHIVE " + derp.getBlockState());
            }
        }
    }

    private void logEvent(Event event) {
        LOGGER.info("Got event" + event.getClass().getName());
    }

    // You can use EventBusSubscriber to automatically register all static methods in the class annotated with @SubscribeEvent
    @Mod.EventBusSubscriber(modid = MODID, bus = Mod.EventBusSubscriber.Bus.MOD, value = Dist.CLIENT)
    public static class ClientModEvents
    {
        @SubscribeEvent
        public static void onClientSetup(FMLClientSetupEvent event)
        {
            // Some client setup code
            LOGGER.info("HELLO FROM CLIENT SETUP");
            LOGGER.info("MINECRAFT NAME >> {}", Minecraft.getInstance().getUser().getName());
        }
    }


